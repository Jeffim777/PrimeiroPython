nome = input('Digite seu nome: ')
sobrenome = input('Digite seu sobrenome: ')
cidade = input('Digite sua cidade: ') # o input sozinho é usado para literal (string), ou seja, texto
idade = int(input('Digite sua idade: ')) # int é usado para números inteiros. Exemplo: 3 -6
peso = float(input('Digite seu peso: ')) # float é usado para números com casas decimais. Exemplo: 3.5 -2.567
print('Seu nome completo é:', nome, sobrenome, 'da cidade de', cidade, 'que tem', idade, 'anos e pesa', peso, 'KGs')